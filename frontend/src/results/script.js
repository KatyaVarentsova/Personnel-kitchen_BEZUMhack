// Функция для получения случайного типа личности
function getRandomPersonality() {
  const randomIndex = Math.floor(Math.random() * personalityTypes.length);
  return personalityTypes[randomIndex];
}

// Функция для отображения случайного типа личности в DOM
function displayRandomPersonality() {
  const personality = getRandomPersonality();
  const displayArea = document.getElementById("personality-display");
  if (displayArea) {
    displayArea.innerHTML = `
      <h2 class="personality">${personality.type}</h2>
      <p class="text">${personality.description}</p>
      <button id="agree-button" class="agree-button">Согласен, спасибо!</button>
    `;
  }
}

// При загрузке страницы автоматически выводим случайный тип личности
document.addEventListener("DOMContentLoaded", () => {
  displayRandomPersonality();
});






document.addEventListener("DOMContentLoaded", () => {
  const agreeButton = document.getElementById("agree-button");

  // Функция для показа тултипа
  function showTooltip() {
    const tooltip = document.createElement("div");
    tooltip.className = "tooltip";
    tooltip.textContent = "Не трогай меня, я тебе не кнопка какая-нибудь";
    document.body.appendChild(tooltip);

    const buttonRect = agreeButton.getBoundingClientRect();
    tooltip.style.position = "absolute";
    tooltip.style.left = buttonRect.left + "px";
    tooltip.style.top = (buttonRect.top - 30) + "px";
    tooltip.style.zIndex = "1000";

    // Удаляем тултип через 1 секунду
    setTimeout(() => {
      tooltip.remove();
    }, 1000);
  }

  agreeButton.addEventListener("mouseenter", () => {
    showTooltip();
  });
  agreeButton.addEventListener("click", () => {
    window.location.href = "success.html";
  });
});
